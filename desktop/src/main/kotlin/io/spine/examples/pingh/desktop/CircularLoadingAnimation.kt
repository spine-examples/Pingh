package io.spine.examples.pingh.desktop

import androidx.compose.animation.core.LinearEasing
import androidx.compose.animation.core.animateFloat
import androidx.compose.animation.core.infiniteRepeatable
import androidx.compose.animation.core.rememberInfiniteTransition
import androidx.compose.animation.core.tween
import androidx.compose.foundation.layout.Box
import androidx.compose.foundation.layout.fillMaxSize
import androidx.compose.material3.CircularProgressIndicator
import androidx.compose.runtime.Composable
import androidx.compose.runtime.getValue
import androidx.compose.ui.Modifier
import androidx.compose.ui.graphics.Color
import androidx.compose.ui.graphics.graphicsLayer
import androidx.compose.ui.unit.Dp
import androidx.compose.ui.unit.dp

/**
 * Displays an animated circular loading indicator.
 *
 * @param color The color of this loading indicator.
 * @param strokeWidth The stroke width of this loading indicator
 * @param durationMillis The duration of the animation.
 * @param modifier The modifier to be applied to the box containing the loading indicator.
 */
@Composable
internal fun CircularLoadingAnimation(
    color: Color,
    strokeWidth: Dp = 1.dp,
    durationMillis: Int = 1000,
    modifier: Modifier = Modifier
) {
    val infiniteTransition = rememberInfiniteTransition()
    val rotation by infiniteTransition.animateFloat(
        initialValue = 0f,
        targetValue = 360f,
        animationSpec = infiniteRepeatable(
            animation = tween(durationMillis = durationMillis, easing = LinearEasing),
        )
    )
    Box(modifier) {
        CircularProgressIndicator(
            modifier = Modifier.fillMaxSize()
                .graphicsLayer {
                    rotationZ = rotation
                },
            color = color,
            strokeWidth = strokeWidth
        )
    }
}
